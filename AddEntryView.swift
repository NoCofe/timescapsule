    // ÂºÄÂßãÂΩïÈü≥
    private func startRecording() {
        let audioSession = AVAudioSession.sharedInstance()
        
        do {
            try audioSession.setCategory(.playAndRecord, mode: .default)
            try audioSession.setActive(true)
            
            let documentPath = FileManager.default.urls(for: .documentDirectory, in: .userDomainMask)[0]
            let audioFilename = documentPath.appendingPathComponent("\(UUID().uuidString).m4a")
            
            let settings = [
                AVFormatIDKey: Int(kAudioFormatMPEG4AAC),
                AVSampleRateKey: 44100,
                AVNumberOfChannelsKey: 1,
                AVEncoderAudioQualityKey: AVAudioQuality.high.rawValue
            ]
            
            audioRecorder = try AVAudioRecorder(url: audioFilename, settings: settings)
            audioRecorder?.record()
            
            audioURL = audioFilename
            isRecording = true
            recordingTime = 0
            
            // ÂêØÂä®ËÆ°Êó∂Âô®
            timer = Timer.scheduledTimer(withTimeInterval: 1, repeats: true) { _ in
                self.recordingTime += 1
            }
        } catch {
            print("ÂΩïÈü≥Â§±Ë¥•: \(error.localizedDescription)")
        }
    }
    
    // ÂÅúÊ≠¢ÂΩïÈü≥
    private func stopRecording() {
        audioRecorder?.stop()
        timer?.invalidate()
        isRecording = false
        
        // Ëé∑ÂèñÂΩïÈü≥Êó∂Èïø
        if let url = audioURL {
            do {
                let audioPlayer = try AVAudioPlayer(contentsOf: url)
                audioDuration = audioPlayer.duration
            } catch {
                print("Ëé∑ÂèñÂΩïÈü≥Êó∂ÈïøÂ§±Ë¥•: \(error.localizedDescription)")
            }
        }
    }
    
    // ÂèñÊ∂àÂΩïÈü≥
    private func cancelRecording() {
        audioRecorder?.stop()
        timer?.invalidate()
        isRecording = false
        
        // Âà†Èô§Êñá‰ª∂
        if let url = audioURL {
            try? FileManager.default.removeItem(at: url)
        }
        
        audioURL = nil
    }
    
    // Âà†Èô§ÂΩïÈü≥
    private func deleteRecording() {
        audioPlayer?.stop()
        
        if let url = audioURL {
            try? FileManager.default.removeItem(at: url)
        }
        
        audioURL = nil
    }
    
    // Êí≠Êîæ/ÊöÇÂÅúÂΩïÈü≥
    private func togglePlayback() {
        if let player = audioPlayer {
            if player.isPlaying {
                player.pause()
            } else {
                player.play()
            }
        } else if let url = audioURL {
            do {
                audioPlayer = try AVAudioPlayer(contentsOf: url)
                audioPlayer?.delegate = ContextWrapper(context: self).avAudioPlayerDelegate
                audioPlayer?.play()
            } catch {
                print("Êí≠ÊîæÂ§±Ë¥•: \(error.localizedDescription)")
            }
        }
    }
    
    // Ëé∑ÂèñÊí≠ÊîæËøõÂ∫¶
    private func getPlaybackProgress() -> CGFloat {
        guard let player = audioPlayer, player.duration > 0 else { return 0 }
        return CGFloat(player.currentTime / player.duration)
    }
    
    // Ê†ºÂºèÂåñÊó∂Èïø
    private func formatDuration(_ duration: TimeInterval) -> String {
        let minutes = Int(duration) / 60
        let seconds = Int(duration) % 60
        return String(format: "%02d:%02d", minutes, seconds)
    }
    
    // ‰øùÂ≠òËÆ∞ÂΩï
